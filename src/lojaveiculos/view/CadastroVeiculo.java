    public void edit() {

        DefaultTableModel modelo = (DefaultTableModel) (jTVeiculos.getModel());

        try {
            String selected = (String) modelo.getValueAt(jTVeiculos.getSelectedRow(), 0);
            MVeiculos v = new MVeiculos();
            v.setPlaca(selected);

            EditarVeiculo editarVeiculo = new EditarVeiculo();
            editarVeiculo.setVisible(true);

            VeiculoDAO vdao = new VeiculoDAO();
            ArrayList<MVeiculos> veiculos = vdao.consultaPorPlaca(v.getPlaca());

            ArrayList<JTextField> txt = editarVeiculo.getTextFields();

            for (MVeiculos vec : veiculos) {
                txt.get(0).setText(vec.getPlaca());
                txt.get(0).setEnabled(false);
                txt.get(1).setText("" + vec.getAnoFab());
                txt.get(2).setText("" + vec.getPreco());
                txt.get(3).setText("" + vec.getUf());
                txt.get(4).setText(vec.getMarca());
                txt.get(5).setText(vec.getModelo());
                txt.get(6).setText(vec.getLoja());
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Antes de pressionar o botão Editar, selecione uma linha da tabela");
        }
    }

    public void eliminar() {

        DefaultTableModel modelo = (DefaultTableModel) (jTVeiculos.getModel());

        try {
            String selected = (String) modelo.getValueAt(jTVeiculos.getSelectedRow(), 0);

            VeiculoDAO vdao = new VeiculoDAO();
            ArrayList<MVeiculos> veiculo = vdao.consultaPorPlaca(selected);

            VeiculoDAO eliminarVeiculo = new VeiculoDAO();
            eliminarVeiculo.Eliminar(veiculo.get(0));

            System.out.println();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Antes de pressionar o botão Eliminar, selecione uma linha da tabela");
        }

    }

