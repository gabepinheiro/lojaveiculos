    public void insert() {

        DefaultTableModel modelo = (DefaultTableModel) jTVeiculos.getModel();
        jTVeiculos.setRowSorter(new TableRowSorter(modelo));

        MVeiculos v = new MVeiculos();
        VeiculoDAO dao = new VeiculoDAO();

        v.setPlaca(txtPlaca.getText());
        v.setAnoFab(Integer.parseInt(txtAno.getText()));
        v.setPreco(Float.parseFloat(txtPreco.getText()));
        v.setUf(listUF.getSelectedValue());
        v.setMarca(listMarca.getSelectedValue());
        v.setModelo(listModelo.getSelectedValue());
        v.setLoja(listLoja.getSelectedValue());

        dao.create(v);
        readJTable();
        clearCampos();

    }

    public void readJTable() {

        ((DefaultTableModel) jTVeiculos.getModel()).setRowCount(0);
        DefaultTableModel modelo = (DefaultTableModel) jTVeiculos.getModel();

        VeiculoDAO vdao = new VeiculoDAO();

        for (MVeiculos v : vdao.read()) {
            modelo.addRow(new Object[]{
                v.getPlaca(),
                v.getAnoFab(),
                v.getPreco(),
                v.getUf(),
                v.getMarca(),
                v.getModelo(),
                v.getLoja(),});
        }

    }

