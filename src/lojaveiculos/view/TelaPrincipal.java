    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        // TODO add your handling code here:

        CadastroVeiculo vframe = new CadastroVeiculo();
        vframe.setVisible(true);

        /* ------------ ESSE TRECHO DE CODIGO PREENCHE O JList DE ESTADOS -------------- */
        DefaultListModel listuf = (DefaultListModel) (vframe.listUF().getModel());
        EstadoDAO estadodao = new EstadoDAO();
        
        ArrayList<MEstado> ufs = estadodao.read();
        
        for (MEstado e : ufs) {
            listuf.addElement(e.getSigla());
        }
        /* ------------ FIM ESTADOS -------------- */

 /* ------------ ESSE TRECHO DE CODIGO PREENCHE O JList DE MARCAS -------------- */
        DefaultListModel listmarcas = (DefaultListModel) (vframe.listMarca().getModel());
        MarcaModeloDAO mmdao = new MarcaModeloDAO();
        
        ArrayList marcas = mmdao.buscarMarcas();
        
        for (Object m : marcas) {
            listmarcas.addElement(m);
        }

        /* ------------ ESSE TRECHO DE CODIGO PREENCHE O JList DE MARCAS -------------- */
        DefaultListModel listlojas = (DefaultListModel) (vframe.listLoja().getModel());
        LojaDAO lojas = new LojaDAO();
        
        ArrayList<MLoja> ls = lojas.buscarLojas();
        
        for (int i = 0; i < ls.size(); i++) {
            listlojas.addElement(ls.get(i).getIdLoja());
            
        }
        /* ------------ FIM LOJAS -------------- */
    }//GEN-LAST:event_jMenuItem1ActionPerformed

